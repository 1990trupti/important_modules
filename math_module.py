import math

print(math.sqrt(25))

print(math.ceil(56.4))
print(math.floor(56.9))

print(math.gcd(24,36))

"""
Euler’s Number 
The math.e constant returns the Euler’s number: 2.71828182846.
              Syntax:  math.e
"""
print(math.e)


# Pi
# syntax ---- math.pi

print(math.pi)

"""
Tau:  Tau is defined as the ratio of the circumference to the radius of a circle. 
The math.tau constant returns the value tau: 6.283185307179586.

            Syntax:   math.tau
"""

print(math.tau)


# Infinity
# math.inf      for positive infinity
# -math.inf     for negative infinity

print(math.inf)
print(-math.inf)

"""
NaN: The math.nan constant returns a floating-point nan (Not a Number) value. 
This value is not a legal number. The nan constant is equivalent to float(“nan”).
"""

print (math.nan)

# factorial():

a = 5
print(math.factorial(a))

# fabs()----------function returns the absolute value of the number

b = 10
print(math.fabs(b))


# exp()----- method is used to calculate the power of e i.e. e^y or we can say exponential of y.

int = 5
